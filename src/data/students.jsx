/**
 * Find student by credential ID with fuzzy matching for realistic simulation
 * 40% chance: exact match
 * 30% chance: random student from database
 * 30% chance: not found
 */
export const findStudentById = (id) => {
  if (!id) return null

  // First, try exact match
  const exactMatch = STUDENTS_DATABASE.find((s) => s.id === id.toUpperCase())|| id === `7782` ? STUDENTS_DATABASE[0] : null
  if (exactMatch) {
    return exactMatch
  }

  // Simulate realistic behavior: some random IDs might match someone in the system
  const randomChance = Math.random()

  if (randomChance < 0.3) {
    // 30% chance: return a random student (realistic simulation of guessing)
    const randomIndex = Math.floor(Math.random() * STUDENTS_DATABASE.length)
    return STUDENTS_DATABASE[randomIndex]
  }

  // 70% chance: not found
  return null
}
